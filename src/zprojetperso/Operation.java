package zprojetperso;

public class Operation {



	public void Addition(int a, int b) {
		int somme = a+b;
		System.out.println("La somme de "+a+" et "+b+" est de "+somme);		
	}



public void Difference(int a, int b) {
	int difference = a-b;
	System.out.println("La difference de "+a+" et "+b+" est de "+difference);		
}



public void Multiplication(int a, int b) {
	int produit = a*b;
	System.out.println("La produit de "+a+" et "+b+" est de "+produit);		
}

}
